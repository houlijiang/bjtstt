  <h4>北京率先运用“环氧树脂涂层钢筋”技术</h4>
  <div class="date">更新时间：2011-03-27</div>

  <p>北京在城市建设中的率先运用“环氧树脂涂层钢筋”技术，解决了除冰盐锈蚀钢筋的难题，受到业内重视。 <p>“环氧树脂涂层钢筋”在国外已得到普遍应用。据专家介绍，建筑物混凝土中的钢筋因碱性的水泥具有腐蚀性，以及潮湿空气中的二氧化碳等酸性气体通过水泥空隙对其的侵蚀，经过一定时间可能产生隐患，甚至使建筑物就并逐步沦为危房，缩短建筑物的使用寿命。因此美、欧、日等国家对此十分重视，早就通过立法制定建筑钢筋保护的标准，规定用于建筑物的钢筋，在使用前必须进行特种粉末涂料喷涂。目前我国混凝土建筑物的钢筋涂层保护尚未起步，只是在斜拉索大桥的钢索保护上逐步由油脂保护转向了功能涂料保护，但所用产品均为进口。造成这种现状的原因主要是进口产品成本太高，国内技术不能过关，以及观念上的落后。而现在国内已生产出这种高科技产品，应用前景广阔。 北京是在铁路西站南广场立交工程中应用“环氧树脂涂层钢筋”技术的，除冰盐锈蚀钢筋的难题的解决，为我国推广这项技术提供了范例。据悉，建设部和国家技术监督局已着手制订建筑钢筋涂层保护的标准，并将在1～3年内在高层建筑中强制实施。中国环氧树脂行业协会的有关人士说，他们将为推广这一技术提供服务，为建筑物安全作出贡献。 <meta name="ContentEnd"></p>  </div> 
